def reverse_string(a):
    return a[::-1]

sample_string="hello world"
up_string="HELLO WORLD"
print("Original: ", sample_string)
print(f"Reversed: {reverse_string(sample_string)}")
print("Capitalize: ", sample_string.capitalize())
print("Lowercase: ",up_string.lower())
print("Uppercase: ", sample_string.upper())
